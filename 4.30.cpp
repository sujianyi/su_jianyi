#include <iostream>

using namespace std;

double r;
int main()
{

    cout << "Please enter the radius of the circle" ;
    cin>>r;
    cout<<"The diameter of a circle is"<<r*2 << endl;
    cout<<"The perimeter of a circle is"<<2*3.14159*r<<endl;
    cout<<"The area of a circle is"<<3.14159*r*r<< endl;

    return 0;
}
